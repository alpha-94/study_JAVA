package exam06;

public class BooleanType {

	public static void main(String[] args) {

		
		System.out.println(35>25); //true
		
		System.out.println(35<25);//false
		
		boolean bool1;
		boolean bool2;
		
		bool1 = 35>25;
		bool2 = 35<25;
		
		System.out.println(bool1);
		System.out.println(bool2);
		
		
		
	}

}
